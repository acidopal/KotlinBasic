fun main(args:Array<String>){
    //Condition TRUE
    //Karena 3 kurang dari 6
    println(3<6)

    //Condition FALSE
    //Karena kondisi 3 < 10 True , dan & > 10 False
    //True + False = False
    println(3<10 && 8>10)

    //Condition TRUE
    //Kondisi 3 < 10 True , dan & > 10 False
    //Karena menggunakan OR jika salah satu ada yg bernilai true maka hasilnya true
    println(3<10 || 8>10)
}